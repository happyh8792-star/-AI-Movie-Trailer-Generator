def make_title_card(title, subtitle, duration, card_type="intro"):
    canvas = Image.new("RGB", (OUTPUT_W, OUTPUT_H), (0, 0, 0))
    draw   = ImageDraw.Draw(canvas)

    # ── Deep cinematic gradient (very dark) ───────────────
    for y in range(OUTPUT_H):
        ratio = y / OUTPUT_H
        r = int(5 + 15 * (1 - ratio))
        g = int(0 + 5  * (1 - ratio))
        b = int(10 + 20 * (1 - ratio))
        draw.line([(0, y), (OUTPUT_W, y)], fill=(r, g, b))

    # ── Subtle vignette edges (darker corners) ────────────
    for i in range(80):
        alpha = int(180 * (1 - i/80))
        draw.rectangle([i, i, OUTPUT_W-i, OUTPUT_H-i],
                       outline=(0, 0, 0))

    # ── Horizontal light streak across middle ─────────────
    for i in range(3):
        y = OUTPUT_H // 2 - 40 + i * 80
        draw.line([(0, y), (OUTPUT_W, y)],
                  fill=(30, 20, 10), width=1)

    # ── Thin gold border lines ────────────────────────────
    gold = (180, 140, 40)
    draw.line([(60, 30), (OUTPUT_W-60, 30)],    fill=gold, width=1)
    draw.line([(60, OUTPUT_H-30), (OUTPUT_W-60, OUTPUT_H-30)], fill=gold, width=1)

    # ── Small corner marks ────────────────────────────────
    cs = 20
    for cx, cy in [(60,30),(OUTPUT_W-60,30),(60,OUTPUT_H-30),(OUTPUT_W-60,OUTPUT_H-30)]:
        draw.rectangle([cx-2, cy-2, cx+2, cy+2], fill=gold)

    # ── Fonts ─────────────────────────────────────────────
    try:
        fp = "/usr/share/fonts/truetype/liberation/LiberationSans-Bold.ttf"
        fr = "/usr/share/fonts/truetype/liberation/LiberationSans-Regular.ttf"
        ft = ImageFont.truetype(fp, 100)   # big bold title
        fs = ImageFont.truetype(fr, 22)    # thin subtitle
        fc = ImageFont.truetype(fr, 13)    # tiny details
        fl = ImageFont.truetype(fp, 14)    # small caps genre
    except:
        ft = fs = fc = fl = ImageFont.load_default()

    # ── TITLE — all caps, gold, centered ─────────────────
    title_upper = title.upper()
    bbox = ft.getbbox(title_upper)
    tw = bbox[2] - bbox[0]

    # Shadow layers for depth
    for offset in [(4,4),(3,3),(2,2)]:
        draw.text(((OUTPUT_W-tw)//2 + offset[0],
                   OUTPUT_H//2 - 80 + offset[1]),
                  title_upper, font=ft, fill=(20, 10, 0))

    # Main gold title
    draw.text(((OUTPUT_W-tw)//2, OUTPUT_H//2 - 80),
              title_upper, font=ft, fill=(220, 180, 50))

    # Subtle white highlight on top of title
    draw.text(((OUTPUT_W-tw)//2 - 1, OUTPUT_H//2 - 81),
              title_upper, font=ft, fill=(255, 230, 120))

    # ── Thin divider line ─────────────────────────────────
    lw = min(tw + 40, OUTPUT_W - 120)
    lx = (OUTPUT_W - lw) // 2
    draw.line([(lx, OUTPUT_H//2 + 30), (lx + lw, OUTPUT_H//2 + 30)],
              fill=(180, 140, 40), width=1)

    # ── Subtitle — spaced out thin text ──────────────────
    subtitle_upper = subtitle.upper()
    # Add letter spacing effect
    spaced = "  ".join(subtitle_upper)
    bbox2 = fs.getbbox(spaced)
    sw = bbox2[2] - bbox2[0]
    draw.text(((OUTPUT_W - sw) // 2, OUTPUT_H//2 + 42),
              spaced, font=fs, fill=(180, 170, 150))

    # ── Genre tag (intro only) ────────────────────────────
    if card_type == "intro":
        tag = genre.upper()
        spaced_tag = " · ".join(tag.split())
        bbox3 = fl.getbbox(spaced_tag)
        gw = bbox3[2] - bbox3[0]
        draw.text(((OUTPUT_W - gw) // 2, OUTPUT_H//2 - 115),
                  spaced_tag, font=fl, fill=(150, 130, 80))

        # Small line above genre
        draw.line([((OUTPUT_W-gw)//2 - 20, OUTPUT_H//2 - 120),
                   ((OUTPUT_W+gw)//2 + 20, OUTPUT_H//2 - 120)],
                  fill=(100, 80, 30), width=1)

    # ── Bottom credits line ───────────────────────────────
    credit = "AN  AI  GENERATED  FILM  EXPERIENCE"
    bbox4 = fc.getbbox(credit)
    cw = bbox4[2] - bbox4[0]
    draw.text(((OUTPUT_W - cw) // 2, OUTPUT_H - 45),
              credit, font=fc, fill=(80, 70, 50))

    # ── Year (outro only) ─────────────────────────────────
    if card_type == "outro":
        year = "2025"
        byear = fc.getbbox(year)
        yw = byear[2] - byear[0]
        draw.text(((OUTPUT_W - yw) // 2, OUTPUT_H//2 + 75),
                  year, font=fc, fill=(120, 100, 60))

    return ImageClip(np.array(canvas)).set_duration(duration).fadein(1.2).fadeout(1.0)

