def add_vignette(clip, color="dark", intensity=0.7):
    import numpy as np

    def vignette_frame(frame):
        img = frame.astype(np.float32)
        h, w = img.shape[:2]

        Y, X = np.ogrid[:h, :w]
        cx, cy = w//2, h//2
        dist = np.sqrt(((X-cx)/cx)**2 + ((Y-cy)/cy)**2)
        mask = np.clip(1 - dist * intensity, 0, 1)

        if color == "red":
            img[:,:,0] *= np.clip(mask + 0.3, 0, 1)
            img[:,:,1] *= mask
            img[:,:,2] *= mask
        else:
            for c in range(3):
                img[:,:,c] *= mask

        return np.clip(img, 0, 255).astype(np.uint8)

    return clip.fl_image(vignette_frame)

